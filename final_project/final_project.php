<h2>Final Project</h2>
<h3 class="note">ABSOLUTE DEADLINE: Friday, May 11th at 5 PM</h3>

<h3>Overview</h3>
<p>This is the same final project that produced a <span class="title">2600 Magazine</span> article!</p>
<p>We have covered a plethora of security topics in this class.  For the final project in the class, you will delve into a current security issue of your choice.  This is not only your opportunity to specialize on a topic, but also to stand out and gain hands-on experience in the field.</p>

<h3>Outcomes</h3>
<p>My hope of this final project is that you will:</p>
<ol>
<li>Specialize in a current security issue of your choice</li>
<li>Gain hands-on experience in the field</li>
<li>Demonstrate technical knowledge</li>
<li>Communicate clearly to a community</li>
</ol>

<h3>Instructions</h3>
<p>For this final project, you must:</p>
<ol>
<li>Choose a mentor</li>
<li>Write a paper</li>
<li>Provide your own supporting material that complements your paper</li>
</ol>

<h3>The Mentor</h3>
<p>The mentor can either me, Ben, Andrew, or someone in the industry.  The mentor cannot be a student in the class.  Should you choose someone other than me as your mentor, please send me the person's name, title, and e-mail address.</p>

<h3>The Paper</h3>
<p>The following are the guidelines for the paper:</p>
<ul>
<li>The paper must be at least 2000 words, <span class="stress">double-spaced</span></li>
<li>The first page (which does not count towards the 2000 words minimum), must be a cover page in the following format:
<pre>
Title
Your Name
Your E-Mail Address
Your Mentor's Name
</pre>
</li>
<li>Required sections of paper:
<ul>
<li><span class="stress">Abstract</span></li>
<li><span class="stress">Introduction</span></li>
<li><span class="stress">To the Community</span> - Why did you choose this topic?  Why is this important to know?</li>
<li>One of the following: <span class="stress">Action Items</span>, <span class="stress">Defenses</span>, <span class="stress">Applications</span></li>
<li><span class="stress">Conclusion</span> or <span class="stress">Summary</span></li>
<li><span class="stress">References</span> - You must use at least five (5) high-quality references.  <span class="note">If you cite Wikipedia as a reference, you will receive an F for the final project.</span></li>
</ul>
</li>
</ul>

<h3>The Supporting Material</h3>
<p>Along with your paper, <span class="stress">you must produce (i.e., you must write)</span> a supporting material for your paper.  Options:</p>
<ol>
<li>Source code: This can be a program (demo acceptable), an exploit, a new security tool, an extension to an existing security tool or product, a script, a proof-of-concept, etc.  You can use any programming language and there is no requirement for lines-of-code that you have to write.</li>
<li>A video: Must be at least five (5) minutes long, most likely a demo</li>
</ol>

<h3>Ideas</h3>
<ul>
<li>Security visualization</li>
<li>Securing online games</li>
<li>The Russian Business Network (RBN)</li>
<li>iPhone Forensics</li>
<li>Android Forensics</li>
<li>Something pertaining to Privacy Enhancing Technology (PET)</li>
<li>Modern malware development</li>
<li>RFID hacking</li>
<li>Web application firewalls</li>
<li>Stuxnet</li>
<li>Creating wargames</li>
</ul>

<h3>Unacceptable Ideas</h3>
<ul>
<li>Modding a video game console</li>
</ul>

<h3>Grading</h3>
<p>Your final project will be graded on the following:</p>
<ul>
<li>Paper (75%)
<ol>
<li>Abstract (5%; see below)</li>
<li>Outline (5%; see below)</li>
<li>Content and structure</li>
<li>Language</li>
<li>References</li>
<li>Knowledge and wisdom</li>
<li>Community service</li>
<li>State-of-the-art</li>
</ol>
</li>
<li>Supporting Material (25%)</li>
</ul>
<p>See <a href="http://www.giac.org/media/certifications/gold/gold_rubric.pdf" target="_blank">http://www.giac.org/media/certifications/gold/gold_rubric.pdf</a> for the rubric I will use for grading the paper</p>

<h3>Deliverables</h3>
<ul>
<li><span class="note">(Due in class on Monday, April 9th)</span> Abstract.  Your abstract shall not exceed 250 words.  Please hand in, on paper, in class. Also, note the mentor who you will use for the project.  If you do not note the mentor, I assume it will be me.</li>
<li><span class="note">(Due in class on Wednesday, April 18th)</span> High-level outline of your paper and provide an idea for your supporting material.</li>
<li><span class="note">(Due on Friday, May 11th at 5 PM)</span> Your entire final project.  Submit all your files via <code>provide</code>.  That is, <code>provide comp116 finalproj *</code>.  You are allowed a maximum of two (2) submissions.  The entire submission must not exceed 20 MB.  Finally, <span class="stress">no submission will be accepted after 5 PM on Friday, May 11th and you will receive a 10 for the final project!</span></ul>
